// Hook trilíngue local. NÃO usa o LanguageContext global porque o seletor de idioma
// nas páginas públicas é embutido (cliente escolhe ali mesmo).
export function usePublicGlobalI18n(initialLang = 'en') {
  const [lang, setLang] = useState(() => {
    if (typeof window === 'undefined') return initialLang;
    const params = new URLSearchParams(window.location.search);
    return params.get('lang') || initialLang;
  });

  useEffect(() => {
    if (typeof window !== 'undefined') {
      const url = new URL(window.location.href);
      url.searchParams.set('lang', lang);
      window.history.replaceState({}, '', url.toString());
    }
  }, [lang]);

  const t = useMemo(() => {
    const dict = DICT[lang] || DICT.en;
    return (key, vars) => {
      let str = dict[key] || DICT.en[key] || key;
      if (vars) Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, v);
      });
      return str;
    };
  }, [lang]);

  return { lang, setLang, t };
}

